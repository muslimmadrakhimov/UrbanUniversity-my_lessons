# Работа со словарями

# 1. Создание словаря my_dict
my_dict = {
    "Алексей": 1990,
    "Мария": 1985,
    "Иван": 2000
}

# 2. Вывод словаря my_dict
print("Словарь my_dict:", my_dict)

# 3. Вывод значения по существующему и отсутствующему ключу (с использованием метода get, чтобы избежать ошибки)
print("Значение по существующему ключу 'Алексей':", my_dict.get("Алексей"))
print("Значение по отсутствующему ключу 'Сергей':", my_dict.get("Сергей", "Ключ отсутствует"))

# 4. Добавление двух новых пар в словарь
my_dict["Ольга"] = 1995
my_dict["Дмитрий"] = 1992

# 5. Удаление пары по существующему ключу и вывод значения
removed_value = my_dict.pop("Мария", "Ключ отсутствует")
print("Удалённое значение по ключу 'Мария':", removed_value)

# 6. Вывод изменённого словаря my_dict
print("Изменённый словарь my_dict:", my_dict)

# Работа с множествами

# 1. Создание множества my_set с повторяющимися значениями
my_set = {1, 2, 2, 3, 4, "apple", "banana", "apple", True, False, None}

# 2. Вывод множества (только уникальные значения)
print("Множество my_set:", my_set)

# 3. Добавление двух новых элементов
my_set.add(5)
my_set.add("orange")

# 4. Удаление одного элемента
my_set.discard(3)

# 5. Вывод изменённого множества my_set
print("Изменённое множество my_set:", my_set)
